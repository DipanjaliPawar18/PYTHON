#WEEK DAYS
# this program will print week day's name corresponding to the input
d= input("enter the number:")
if d==1 :
    print("Sunday")
elif d==2 :
    print("Monday")
elif d==3 :
    print("Tuesday")
elif d==4 :
    print("Wednesday")
elif d==5 :
    print("Thursday")
elif d==6 :
    print("Friday")
elif d==7 :
    print("Saturday")
else:
    print("incorrect choice")